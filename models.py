class User:
    def __init__(self, id, username, first_name, last_name):
        self.id = id
        self.username = username
        self.first_name = first_name
        self.last_name = last_name

class Channel:
    def __init__(self, id, name, link):
        self.id = id
        self.name = name
        self.link = link
